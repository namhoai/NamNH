    public DonThuocFrm(BenhAn benhAn) {
        initComponents();

        this.benhAn = benhAn;
        
        donThuoc = benhAn.getDonThuoc();
        
        if (donThuoc != null) {
            // nếu có dữ liệu thì vẽ lên.
            drawDT(donThuoc);
        }
        
        // Lấy dữ liệu thuốc trong CSDL.
        DonThuocController donThuocController = new DonThuocController();
        dsThuoc = donThuocController.getDSThuoc();
        
        if (dsThuoc != null) {
            drawThuoc(dsThuoc);
        }
        
    }

    private void drawThuoc(List<Thuoc> dsThuoc) {
        for (Thuoc thuoc : dsThuoc) {
            cbb_thuoc.addItem(thuoc.getTen());
        }
    }
    
    private void drawDT(List<ThuocDung> donThuoc) {
        modelDT = (DefaultTableModel) tbl_donThuoc.getModel();
        while (modelDT.getRowCount() > 0) {
            modelDT.removeRow(0);
        }
        
        int i = 1;
        
        for (ThuocDung thuocDung : donThuoc) {

            modelDT.addRow(new Object[]{
                i++,
                thuocDung.getThuoc().getTen(),
                thuocDung.getSoLuong(),
                thuocDung.getCachSD(),
            });
        }
    }
    
