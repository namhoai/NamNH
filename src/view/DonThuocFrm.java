    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        if (selectedDT == -1) {
            return;
        }

        // Câp nhập lại CSDL.
        DonThuocController donThuocController = new DonThuocController();
        donThuocController.deleteThuocDung(donThuoc.get(selectedDT).getId());

        // Cập nhập lại đơn thuốc.
        donThuoc.remove(selectedDT);
        drawDT(donThuoc);
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        // TODO add your handling code here:
        // Lấy dữ liệu.
        Thuoc thuoc = dsThuoc.get(cbb_thuoc.getSelectedIndex());
        int soLuong = Integer.parseInt(txt_soLuong.getText());
        String cachSD = txt_CSD.getText();
        float chiPhi = soLuong * thuoc.getGia();
        ThuocDung thuocDung = new ThuocDung(0, thuoc, soLuong, chiPhi, cachSD);
        
        // Cập nhập CSDL.
        DonThuocController donThuocController = new DonThuocController();
        donThuocController.addThucDung(thuocDung, this.benhAn.getId());
        
        // Cập nhập đơn thuốc.
        donThuoc.add(thuocDung);
        drawDT(donThuoc);
        txt_CSD.setText("");
        txt_soLuong.setText("");
    }//GEN-LAST:event_btn_themActionPerformed

